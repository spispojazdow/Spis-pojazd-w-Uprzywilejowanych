document.getElementById('wojewodztwo').addEventListener('change', function() {
    const wybrane = this.value;
    const lista = document.getElementById('wyniki');
    const naglowek = document.getElementById('naglowek-lista');

    if (!wybrane || !dane[wybrane]) {
        lista.innerHTML = '';
        naglowek.textContent = 'Wybierz województwo';
        return;
    }

    naglowek.textContent = wybrane;
    lista.innerHTML = dane[wybrane]
        .map(item => `<li><span class="kod">${item.kod}</span><span class="miasto">${item.miasto}</span></li>`)
        .join('');
});
